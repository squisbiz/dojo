				<p>According to Wikipedia, A dojo is a Japanese term which literally means "place of the way".<br>
In the Western World, the term dojo primarily refers to a training place, usually meant for Japanese martial arts. The term can also refer to a formal training place for any of the Japanese arts ending in "do", meaning "way".<br><br>
Inspired by this idea, and by the perfection-seeking performance of the martial artists, we have named this site The Dojo, a community-building online meeting place and training venue for software engineers to practice and hone their English-based communication and soft skills in a professional context. Just like a martial artists would do at a dojo.<br><br>
The Dojo has been conceived for those who already have conversational command of the English language. It contains tools and resources to help software engineers and other professionals to help them hone their communication and soft skills, and also features weekly live streaming talks with special guests.</p>
