				<p>As a leading software company in Mexico catering to Silicon Valley-based clients, English comprises a good chunk of our very bread and butter. To make sure our communication skills remain seamless and up to par with the highest standards, we have created The Dojo, which started out simply as an online conversational club.  Soon thereafter, shorter 30 –minute, 1-on-1 sessions were implemented as a comprehensive English for Special Purposes and Soft Skills training, coaching our developers in English language skills, business etiquette, team building, speaking, presenting, client interaction, and cross-cultural sensitivity.</p>
				
				<p>Now, The Dojo also features DojoLive, a weekly online talk where engineers and other professionals gather to chat, practice English, learn about cultural awareness, hone their soft skills and learn industry insights from leaders and company executives in North America and other countries.</p>
